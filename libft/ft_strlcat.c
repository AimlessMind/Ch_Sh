#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t s)
{
	size_t i;
	size_t dstlen;
	size_t srclen;

	i = 0;
	dstlen = ft_strlen(dst);
	srclen = ft_strlen(src);
	if (s <= dstlen)
		return (srclen + s);
	while ((dst[i] != '\0') && i < (s - 1))
		i++;
	while (*src && i < (s - 1))
	{
		dst[i] = *src;
		i++;
		src++;
	}
	dst[i] = '\0';
	return (dstlen + srclen);
}
