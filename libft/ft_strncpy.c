#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t i;

	i = 0;
	while ((n > 0) && (src[i] != '\0'))
	{
		dst[i] = src[i];
		i++;
		n--;
	}
	while (n--)
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}
